package javacore.Aintroducaoclasses.dominio;

public class Estudante {
    /**
     * <b>Qual a função da orientação a objetos?</b><br>
     * A orientação a objetos foi criada para mapear o mundo real para o mundo computacional, partindo do princípio que as coisas podem ser agrupadas<br><br>

     * <b>Objeto</b> -> diferentes dados sendo guardados em um único espaço em memória<br><br>
     *
     * <b>Classe</b> é o que temos em Java para representar algo do mundo real com funcionalidades em comum<br><br>
     *
     * Os <b>atributos</b> de uma classe geralmente dão origem a um objeto<br><br>
     *
     * As classes que não tem o método main são chamadas de <b>classes de domínio</b>, algo que representa o mapeamento no mundo real<br><br>
     *
     * A classe é um template que vai dar origem a objetos e esse template pode ter <b>atributos(características da classe)</b> e/ou <b>métodos(comportamentos)</b><br>
     */

    public String nome;
    public int idade;
    public char sexo;

}
