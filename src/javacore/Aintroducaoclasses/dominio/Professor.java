package javacore.Aintroducaoclasses.dominio;

public class Professor {
    /**
     * <b>O que é coesão?</b><br>
     * É algo que está relacionado ao propósito das suas classes.<br><br>
     *
     * Quando dizemos que nosso código é altamente coeso, estamos falando que as nossas classes não estão misturando o propósito delas de existirem.<br><br>
     *
     * Código coeso -> propósito único<br>
     * Dê preferência a um propósito por classe<br>
     */

    public String nome;
    public int idade;
    public char sexo;
}
